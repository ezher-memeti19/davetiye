if (momentsForm) {
  const fileInput = momentsForm.querySelector('input[type="file"][name="photos"]');
  let selectedMediaFiles = [];

  const syncFileInput = () => {
    if (!fileInput) return;
    const dataTransfer = new DataTransfer();
    selectedMediaFiles.forEach((file) => dataTransfer.items.add(file));
    fileInput.files = dataTransfer.files;
  };

  const mergeFiles = (incomingFiles) => {
    const nextFiles = Array.from(incomingFiles || []);
    const seen = new Set(selectedMediaFiles.map((file) => `${file.name}:${file.size}:${file.lastModified}`));
    nextFiles.forEach((file) => {
      const key = `${file.name}:${file.size}:${file.lastModified}`;
      if (!seen.has(key)) {
        selectedMediaFiles.push(file);
        seen.add(key);
      }
    });
    syncFileInput();
  };

  const renderPreviewGrid = () => {
    if (!uploadPreviewGrid) return;
    uploadPreviewGrid.innerHTML = "";
    const files = selectedMediaFiles;
    if (!files || files.length === 0) return;

    files.forEach((file, index) => {
      const objectUrl = URL.createObjectURL(file);
      const card = document.createElement("div");
      card.className = "upload-preview-card";

      const removeBtn = document.createElement("button");
      removeBtn.type = "button";
      removeBtn.className = "upload-preview-remove";
      removeBtn.setAttribute("aria-label", `Remove ${file.name}`);
      removeBtn.innerHTML =
        '<svg viewBox="0 0 24 24" focusable="false" aria-hidden="true"><path d="M6 6 18 18M18 6 6 18"/></svg>';
      removeBtn.addEventListener("click", () => {
        selectedMediaFiles = selectedMediaFiles.filter((_, currentIndex) => currentIndex !== index);
        syncFileInput();
        renderSelectedFiles();
      });

      const media = document.createElement(file.type.startsWith("video/") ? "video" : "img");
      if (media.tagName === "VIDEO") {
        media.src = objectUrl;
        media.muted = true;
        media.playsInline = true;
        media.preload = "metadata";
      } else {
        media.src = objectUrl;
        media.alt = file.name;
      }

      media.addEventListener(
        "load",
        () => {
          URL.revokeObjectURL(objectUrl);
        },
        { once: true }
      );

      if (media.tagName === "VIDEO") {
        media.addEventListener(
          "loadeddata",
          () => {
            URL.revokeObjectURL(objectUrl);
          },
          { once: true }
        );
      }

      const meta = document.createElement("div");
      meta.className = "upload-preview-meta";

      const kind = document.createElement("span");
      kind.className = "upload-preview-kind";
      kind.textContent = file.type.startsWith("video/") ? "Video" : "Photo";

      const name = document.createElement("span");
      name.className = "upload-preview-name";
      name.textContent = file.name;

      meta.append(kind, name);
      card.append(removeBtn, media, meta);
      uploadPreviewGrid.appendChild(card);
    });
  };

  const renderSelectedFiles = () => {
    if (!uploadSelected || !fileInput) return;
    const files = selectedMediaFiles;
    if (!files || files.length === 0) {
      uploadSelected.textContent = "";
      renderPreviewGrid();
      return;
    }
    uploadSelected.textContent = files.length === 1 ? files[0].name : `${files.length} files selected`;
    renderPreviewGrid();
  };

  if (fileInput) {
    fileInput.addEventListener("change", () => {
      mergeFiles(fileInput.files);
      renderSelectedFiles();
    });
  }

  if (uploadDropzone && fileInput) {
    ["dragenter", "dragover"].forEach((eventName) => {
      uploadDropzone.addEventListener(eventName, (event) => {
        event.preventDefault();
        uploadDropzone.classList.add("is-dragover");
      });
    });

    ["dragleave", "dragend", "drop"].forEach((eventName) => {
      uploadDropzone.addEventListener(eventName, (event) => {
        event.preventDefault();
        uploadDropzone.classList.remove("is-dragover");
      });
    });

    uploadDropzone.addEventListener("drop", (event) => {
      const droppedFiles = event.dataTransfer?.files;
      if (!droppedFiles || droppedFiles.length === 0) return;
      mergeFiles(droppedFiles);
      renderSelectedFiles();
    });
  }

  momentsForm.addEventListener("submit", async (event) => {
    event.preventDefault();
    const dict = translations[currentLang] || translations.en;
    const submitBtn = momentsForm.querySelector(".moments-submit");
    const files = selectedMediaFiles;
    const maxClientFileSize = 10 * 1024 * 1024;

    if (!files || files.length < 1) {
      showToast(dict.moments_error_files_required);
      return;
    }

    for (const file of files) {
      if (!file.type.startsWith("image/") && !file.type.startsWith("video/")) {
        showToast(dict.moments_error_images_only);
        return;
      }
      if (file.size > maxClientFileSize) {
        showToast(dict.moments_error_size_limit);
        return;
      }
    }

    const formData = new FormData(momentsForm);
    const previousLabel = submitBtn ? submitBtn.textContent : "";
    if (submitBtn) {
      submitBtn.disabled = true;
      submitBtn.textContent = dict.moments_submit_sending;
    }

    try {
      const response = await fetch("/api/photos/upload", {
        method: "POST",
        body: formData
      });

      if (!response.ok) {
        throw new Error("Upload failed");
      }

      momentsForm.reset();
      selectedMediaFiles = [];
      syncFileInput();
      renderSelectedFiles();
      showToast(dict.toast_moments_saved);
    } catch {
      showToast(dict.moments_error_upload_failed);
    } finally {
      if (submitBtn) {
        submitBtn.disabled = false;
        submitBtn.textContent = previousLabel || dict.moments_submit;
      }
    }
  });
}

