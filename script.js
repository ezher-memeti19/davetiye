function renderDigitCards(container, previous, next) {
  if (!container) return;

  const prevText = previous || next;
  const maxLen = Math.max(prevText.length, next.length, 2);
  const prevDigits = prevText.padStart(maxLen, "0").split("");
  const nextDigits = next.padStart(maxLen, "0").split("");

  container.innerHTML = "";
  nextDigits.forEach((digit, index) => {
    const card = document.createElement("span");
    card.className = "flip-card";
    if (prevDigits[index] !== digit) card.classList.add("changed");
    card.innerHTML = `<span class="digit">${digit}</span>`;
    container.appendChild(card);
  });
}

function renderDateFlipcards() {
  renderDigitCards(dateCardEls.day, null, "03");
  renderDigitCards(dateCardEls.month, null, "08");
  renderDigitCards(dateCardEls.year, null, "2026");
}

function showToast(message) {
  if (!toast) return;
  toast.textContent = message;
  toast.classList.add("show");
  setTimeout(() => toast.classList.remove("show"), 2200);
}

function formatIcsDate(date) {
  const pad = (n) => String(n).padStart(2, "0");
  return `${date.getUTCFullYear()}${pad(date.getUTCMonth() + 1)}${pad(date.getUTCDate())}T${pad(
    date.getUTCHours()
  )}${pad(date.getUTCMinutes())}${pad(date.getUTCSeconds())}Z`;
}

function escapeIcs(text) {
  return String(text).replace(/\\/g, "\\\\").replace(/\n/g, "\\n").replace(/,/g, "\\,").replace(/;/g, "\\;");
}

function downloadCalendarFile() {
  const start = new Date("2026-08-03T15:30:00+02:00");
  const end = new Date("2026-08-03T23:00:00+02:00");
  const stamp = formatIcsDate(new Date());
  const uid = `eda-ezher-20260803@davetiye`;
  const ics = [
    "BEGIN:VCALENDAR",
    "VERSION:2.0",
    "PRODID:-//Eda Ezher Wedding//Invitation//EN",
    "CALSCALE:GREGORIAN",
    "METHOD:PUBLISH",
    "BEGIN:VEVENT",
    `UID:${uid}`,
    `DTSTAMP:${stamp}`,
    `DTSTART:${formatIcsDate(start)}`,
    `DTEND:${formatIcsDate(end)}`,
    `SUMMARY:${escapeIcs("Eda & Ezher Wedding")}`,
    `LOCATION:${escapeIcs("Kalkandelen")}`,
    `DESCRIPTION:${escapeIcs("Wedding celebration of Eda and Ezher.")}`,
    "END:VEVENT",
    "END:VCALENDAR"
  ].join("\r\n");

  const blob = new Blob([ics], { type: "text/calendar;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = "eda-ezher-wedding.ics";
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}

function applyLanguage(lang) {
  const dict = translations[lang] || translations.en;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) el.setAttribute("placeholder", dict[key]);
  });

  if (langSelect) langSelect.value = lang;

  currentLang = lang;
  localStorage.setItem("wedding_lang", lang);
}

if (langSelect) {
  langSelect.addEventListener("change", (event) => {
    applyLanguage(event.target.value);
  });
}

if (menuToggle && nav) {
  menuToggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("open");
    menuToggle.setAttribute("aria-expanded", String(isOpen));
    menuToggle.classList.toggle("open", isOpen);
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("open");
      menuToggle.setAttribute("aria-expanded", "false");
      menuToggle.classList.remove("open");
    });
  });

  document.addEventListener("click", (event) => {
    const isOpen = nav.classList.contains("open");
    if (!isOpen) return;
    if (event.target.closest("#nav") || event.target.closest(".menu-toggle")) return;
    nav.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
    menuToggle.classList.remove("open");
  });

  document.addEventListener("keydown", (event) => {
    if (event.key !== "Escape") return;
    nav.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
    menuToggle.classList.remove("open");
  });
}

async function copyTextWithFallback(text) {
  try {
    if (navigator.clipboard && navigator.clipboard.writeText) {
      await navigator.clipboard.writeText(text);
      return true;
    }
  } catch {
    // fallback below
  }

  try {
    const textarea = document.createElement("textarea");
    textarea.value = text;
    textarea.setAttribute("readonly", "");
    textarea.style.position = "absolute";
    textarea.style.left = "-9999px";
    document.body.appendChild(textarea);
    textarea.select();
    const copied = document.execCommand("copy");
    document.body.removeChild(textarea);
    return copied;
  } catch {
    return false;
  }
}

if (copyBtn) {
  copyBtn.addEventListener("click", async () => {
    const dict = translations[currentLang] || translations.en;
    const ok = await copyTextWithFallback(dict.copy_details_text);
    showToast(ok ? dict.toast_copy_success : dict.toast_copy_fail);
  });
}

